-- 1) Core tables
create table if not exists public.user_challenge_progress (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  challenge_id text not null,
  completed_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  unique (user_id, challenge_id)
);

create table if not exists public.pomodoro_sessions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  duration_seconds integer not null check (duration_seconds > 0),
  completed_at timestamptz not null default now(),
  created_at timestamptz not null default now()
);

create table if not exists public.custom_challenges (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  title text not null check (char_length(title) >= 3),
  target_sessions integer not null check (target_sessions > 0),
  points integer not null check (points > 0),
  is_group boolean not null default false,
  created_at timestamptz not null default now()
);

create table if not exists public.reward_catalog (
  id bigint generated by default as identity primary key,
  title text not null,
  description text not null default '',
  cost_points integer not null check (cost_points > 0),
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.reward_redemptions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  reward_id bigint not null references public.reward_catalog(id) on delete restrict,
  points_spent integer not null check (points_spent > 0),
  created_at timestamptz not null default now()
);

create table if not exists public.user_rewards (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  title text not null check (char_length(title) >= 3),
  description text not null default '',
  cost_points integer not null check (cost_points > 0),
  created_at timestamptz not null default now(),
  unique (user_id, title)
);

create table if not exists public.user_leaderboard (
  user_id uuid primary key references auth.users(id) on delete cascade,
  display_name text not null,
  total_points integer not null default 0 check (total_points >= 0),
  updated_at timestamptz not null default now()
);

create table if not exists public.pomodoro_rooms (
  id bigint generated by default as identity primary key,
  owner_id uuid not null references auth.users(id) on delete cascade,
  name text not null check (char_length(name) >= 3),
  created_at timestamptz not null default now()
);

create table if not exists public.pomodoro_room_members (
  room_id bigint not null references public.pomodoro_rooms(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  display_name text not null,
  joined_at timestamptz not null default now(),
  primary key (room_id, user_id)
);

create table if not exists public.pomodoro_room_presence (
  room_id bigint not null references public.pomodoro_rooms(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  time_left integer not null check (time_left >= 0),
  is_active boolean not null default false,
  is_paused boolean not null default false,
  updated_at timestamptz not null default now(),
  primary key (room_id, user_id)
);

create table if not exists public.challenge_catalog (
  id bigint generated by default as identity primary key,
  code text not null unique,
  title text not null check (char_length(title) >= 3),
  description text not null default '',
  week integer not null check (week between 1 and 3),
  day integer not null check (day between 1 and 21),
  points integer not null check (points > 0),
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.push_subscriptions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  endpoint text not null unique,
  p256dh text not null,
  auth text not null,
  subscription jsonb not null,
  user_agent text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

