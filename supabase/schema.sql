-- 1 usuario = 1 sala (limpia duplicados antes de crear constraint)
with ranked_members as (
  select
    room_id,
    user_id,
    joined_at,
    row_number() over (partition by user_id order by joined_at desc, room_id desc) as rn
  from public.pomodoro_room_members
)
delete from public.pomodoro_room_members m
using ranked_members r
where m.room_id = r.room_id
  and m.user_id = r.user_id
  and r.rn > 1;

with ranked_presence as (
  select
    room_id,
    user_id,
    updated_at,
    row_number() over (partition by user_id order by updated_at desc, room_id desc) as rn
  from public.pomodoro_room_presence
)
delete from public.pomodoro_room_presence p
using ranked_presence r
where p.room_id = r.room_id
  and p.user_id = r.user_id
  and r.rn > 1;

create unique index if not exists idx_pomodoro_room_members_user_unique on public.pomodoro_room_members (user_id);
create unique index if not exists idx_pomodoro_room_presence_user_unique on public.pomodoro_room_presence (user_id);

with ranked_rooms as (
  select
    id,
    owner_id,
    created_at,
    row_number() over (partition by owner_id order by created_at desc, id desc) as rn
  from public.pomodoro_rooms
)
delete from public.pomodoro_rooms r
using ranked_rooms rr
where r.id = rr.id
  and rr.rn > 1;

create unique index if not exists idx_pomodoro_rooms_owner_unique on public.pomodoro_rooms (owner_id);

